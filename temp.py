def extract_text_from_annotation(dic):
    text = ""
    pages = dic.get("fullTextAnnotation", {}).get("pages", [])

    for page in pages:
        for block in page.get("blocks", []):
            for para in block.get("paragraphs", []):
                for word in para.get("words", []):
                    for symbol in word.get("symbols", []):
                        text += symbol.get("text", "")
                        # Handle line breaks if present
                        if symbol.get("property", {}).get("detectedBreak", {}).get("type") == "LINE_BREAK":
                            text += "\n"
                        elif symbol.get("property", {}).get("detectedBreak", {}).get("type") == "SPACE":
                            text += " "

                    # Add a space between words if not already handled by break
                    if not word.get("symbols", [])[-1].get("property", {}).get("detectedBreak"):
                        text += " "

    return text.strip()

